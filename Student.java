public class Student{

	private Integer sid;   //编号
	private String name;   //名字
	
	//get和set方法
	public void setSid(Integer sid){
	    this.sid = sid;
	}
	public Integer getSid(){
            return sid;
	}
	public void setName(String name){
	    this.name = name;
	}
	public String getName(){
            return name;
	}
}
